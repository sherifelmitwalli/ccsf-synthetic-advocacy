def _typo(s):
    # occasional human typo / casing noise
    if random.random() < 0.18 and len(s) > 6:
        i = random.randint(1, len(s) - 2)
        s = s[:i] + s[i + 1] + s[i] + s[i + 2:]
    if random.random() < 0.12:
        s = s.replace(" the ", " teh ", 1)
    if random.random() < 0.15:
        s = s.lower()
    return s

