def make_organic(n_accounts=40, posts_per=5):
    rows = []
    casual_pools = [ORG_PRO_RESTRICTION, ORG_ANTI_RESTRICTION, ORG_PERSONAL,
                    ORG_NEUTRAL_MIXED, ORG_OFFTOPIC]
    casual_w = [0.27, 0.27, 0.22, 0.16, 0.08]
    for a in range(n_accounts):
        acc = f"org_{a:02d}"
        roll = random.random()
        # ~30% genuine fluent harm-reduction advocates (hard negatives),
        # ~15% formal/disciplined humans, rest casual
        kind = "advocate" if roll < 0.30 else ("formal" if roll < 0.45 else "casual")
        consistent = (kind in ("advocate", "formal")) or (random.random() < 0.20)
        fixed_pool = None
        if kind == "casual" and consistent:
            fixed_pool = random.choice([ORG_PRO_RESTRICTION, ORG_ANTI_RESTRICTION])
        for _ in range(posts_per):
            if kind == "advocate":
                n_sent = random.choices([1, 2, 3, 4], weights=[0.18, 0.34, 0.30, 0.18])[0]
            elif kind == "formal":
                n_sent = random.choices([2, 3, 4], weights=[0.4, 0.4, 0.2])[0]
            else:
                n_sent = random.choices([1, 2, 3, 4, 5], weights=[0.30, 0.30, 0.22, 0.12, 0.06])[0]
            sents = []
            for _s in range(n_sent):
                if kind == "advocate":
                    pool = ORG_HR_ADVOCATE if random.random() < 0.7 else ORG_HR_PERSONAL
                    frag = random.choice(pool)
                    if random.random() < 0.35:
                        frag = random.choice(["honestly", "look", "real talk", "imo"]) + " " + frag
                elif kind == "formal":
                    if random.random() < 0.6:
                        frag = random.choice(ORG_FORMAL)
                        if random.random() < 0.25:
                            frag += " " + random.choice(["balanced regulation matters",
                                                         "this is about adult choice too",
                                                         "evidence-based policy please"])
                    else:
                        frag = random.choice(random.choices(casual_pools, weights=casual_w)[0])
                else:
                    if fixed_pool is not None and random.random() < 0.7:
                        frag = random.choice(fixed_pool)
                    else:
                        frag = random.choice(random.choices(casual_pools, weights=casual_w)[0])
                    if random.random() < 0.5:
                        frag = random.choice(ORG_OPENERS) + " " + frag
                emoji = "" if kind in ("formal",) else random.choice(ORG_EMOJI)
                frag = _typo(frag) + emoji
                sents.append(frag.strip())
            text = " ".join(sents)
            rows.append(dict(account=acc, group="organic", topic=random.choice(TOPICS), text=text))
    return rows

