SYNTH_SHORT = ["This matters.", "It is that simple.", "The point stands.",
               "Worth remembering.", "Let us be clear."]
# light human-edit perturbations (colloquial asides, contractions, mild noise)
HUMAN_EDITS = [" honestly", " tbh", " imo", " - just saying", " (my view)", " fwiw"]

def _light_edit(text, p=0.5):
    if random.random() < p:
        text = text.replace("should ", "should really ", 1)
    if random.random() < p:
        text = text.rstrip(".") + random.choice(HUMAN_EDITS) + "."
    if random.random() < 0.3:
        text = text.replace("regulation", "regulaton", 1)  # typo
    if random.random() < 0.3:
        text = text[0].lower() + text[1:]
    return text

def _render_synth(tightness, prop_subset=None):
    """tightness in [0,1]: 1 = maximally disciplined campaign, 0 = loose.
    prop_subset: the campaign's messaging repertoire (subset of SYNTH_PROPS keys)."""
    prop_keys = prop_subset if prop_subset else list(SYNTH_PROPS.keys())
    k = random.choices([2, 3], weights=[0.55, 0.45])[0]
    k = min(k, len(prop_keys))
    chosen = random.sample(prop_keys, k)
    sents = []
    for j, pk in enumerate(chosen):
        s = random.choice(SYNTH_PROPS[pk])
        s = s[0].upper() + s[1:]
        if j > 0 and random.random() < (0.4 + 0.5 * tightness):
            s = random.choice(SYNTH_CONNECTORS) + " " + s[0].lower() + s[1:]
        sents.append(s + ".")
    if random.random() < (1 - tightness) * 0.6:
        sents.insert(random.randint(0, len(sents)), random.choice(SYNTH_SHORT))
    if random.random() < (0.55 + 0.4 * tightness):
        sents.append(random.choice(SYNTH_CLOSERS))
    return " ".join(sents)

def make_synth(n_accounts=16, posts_per=10):
    rows = []
    all_keys = list(SYNTH_PROPS.keys())
    for a in range(n_accounts):
        acc = f"syn_{a:02d}"
        tightness = random.uniform(0.45, 1.0)
        edit_p = random.uniform(0.0, 0.35)
        subset = random.sample(all_keys, random.randint(6, 9))  # campaign repertoire
        for _ in range(posts_per):
            text = _render_synth(tightness, prop_subset=subset)
            if random.random() < edit_p:
                text = _light_edit(text, p=0.4)
            rows.append(dict(account=acc, group="synthetic", topic=random.choice(TOPICS), text=text))
    return rows

# ==================================================================================
# (4) HUMAN-EDITED SYNTHETIC ("mixed")  -- the hard, realistic semi-automated case
# ==================================================================================
def make_mixed(n_accounts=10, posts_per=8):
    """Synthetic talking points heavily rewritten by a human: colloquial register,
    varied length, typos, occasional emoji, lower compliance density. Designed to
    sit BETWEEN organic and synthetic and to be difficult to detect."""
    rows = []
    for a in range(n_accounts):
        acc = f"mix_{a:02d}"
        for _ in range(posts_per):
            base = _render_synth(tightness=random.uniform(0.3, 0.7))
            # aggressive human editing
            base = _light_edit(base, p=0.8)
            base = _light_edit(base, p=0.6)
            # break into a more human rhythm: drop a connector, add an opener
            base = base.replace("Moreover, ", "").replace("Crucially, ", "")
            if random.random() < 0.6:
                base = random.choice(ORG_OPENERS) + " " + base[0].lower() + base[1:]
            if random.random() < 0.5:
                base = base.rstrip(".") + random.choice(ORG_EMOJI)
            rows.append(dict(account=acc, group="mixed", topic=random.choice(TOPICS), text=base))
    return rows

