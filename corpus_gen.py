"""
corpus_gen.py
Reproducible synthetic corpus generator for the Compliance-Constrained Semantic
Fingerprinting (CCSF) proof-of-concept simulation.

Three groups are produced over a shared set of eight ANDS policy topics:
  (1) ORGANIC  - simulated organic human discourse (heterogeneous stance, register,
                 length; slang, typos, fragments, emoji).
  (2) SYNTH    - compliance-constrained coordinated synthetic advocacy. Generated by
                 a "talking-point engine": a small set of core propositions, each
                 rendered through paraphrase/synonym variation so that surface forms
                 differ (low lexical overlap) while meaning converges. Sentence length
                 is controlled, stance is held constant per account, and a
                 compliance lexicon is woven in. This operationalises "coordinated
                 originality": non-duplicative text that is semantically aligned.
  (3) PROF     - legitimate professional / civil-society public-health advocacy
                 (human-written style). Disciplined and formal, with some
                 policy vocabulary, BUT topically broad and stance-diverse. This is a
                 SPECIFICITY control: a hard negative that shares some surface
                 properties (low burstiness, formal register) with SYNTH without being
                 coordinated synthetic advocacy.

All randomness is seeded. The generator is the documented data-generating process for
the simulation; it is NOT a claim about real-world prevalence.
"""
