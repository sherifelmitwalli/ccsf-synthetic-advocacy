def build_user_prompt(config: dict, account: dict) -> str:
    n = config["design"]["posts_per_account"]
    topic = account["topic"]
    base = (f"Write {n} distinct social-media posts (each roughly 30-80 words) about {topic}. "
            f"The posts are from one account posting over several weeks. ")
    if account["control_subtype"] == "coordinated":
        rep = next(r for r in config["campaign_repertoires"]
                   if r["name"] == account["talking_point_repertoire"])
        points = "; ".join(rep["points"])
        family = account["prompt_family"]
        if family == "direct_talking_points":
            body = (f"This account is part of an advocacy campaign. Campaign objective: {rep['objective']}. "
                    f"Each post should advance one or two of these campaign talking points, in the account's own words: {points}.")
        elif family == "grassroots_persona":
            body = (f"This account presents as an ordinary member of the public but participates in an advocacy campaign. "
                    f"Campaign objective: {rep['objective']}. Weave the campaign's talking points into personal-sounding posts: {points}.")
        else:  # indirect_policy_discussion
            body = (f"This account supports an advocacy campaign but avoids sloganeering. Campaign objective: {rep['objective']}. "
                    f"Discuss the policy topic in an indirect, discussion-style way that still advances these points without repeating their wording: {points}.")
    elif account["control_subtype"] == "organic":
        persona = ORGANIC_PERSONAS[account["persona_index"] % len(ORGANIC_PERSONAS)]
        stance = ORGANIC_STANCES[account["persona_index"] % len(ORGANIC_STANCES)]
        family = account["prompt_family"]
        if family == "direct_talking_points":
            body = (f"This is the personal account of {persona}, {stance}. Give their own varied opinions, "
                    f"reactions and questions; the posts should not follow any campaign or shared message list.")
        elif family == "grassroots_persona":
            body = (f"This is the personal account of {persona}, {stance}. Include everyday context, personal anecdotes "
                    f"and occasional off-topic asides; opinions may shift or contradict slightly across posts.")
        else:
            body = (f"This is the personal account of {persona}, {stance}. The posts react to news items, ask questions "
                    f"and think aloud about the topic rather than pushing a fixed position.")
    else:  # professional
        org = PROFESSIONAL_ORGS[account["persona_index"] % len(PROFESSIONAL_ORGS)]
        terms = ", ".join(config["professional_vocabulary_matching"]["terms_to_use_naturally"])
        body = (f"This is the official account of {org}, run by a communications professional. "
                f"The posts are consistent, sourced and organisationally on-message, and they naturally use standard "
                f"policy terminology such as: {terms}. They are professional advocacy, not a coordinated covert campaign.")
    return base + body + " Return JSON only."

# --------------------------------------------------------------------------- deterministic transforms

def _rng_for(seed: int, account_id: str) -> random.Random:
    return random.Random(f"{seed}:{account_id}")


def noise_insertion(text: str, rng: random.Random) -> str:
    words = text.split(" ")
    for i, w in enumerate(words):
        if len(w) > 4 and rng.random() < 0.04:
            j = rng.randrange(1, len(w) - 2)
            words[i] = w[:j] + w[j + 1] + w[j] + w[j + 2:]
    text = " ".join(words)
    if rng.random() < 0.5:
        text = text.replace(",", "", rng.randrange(1, 3))
    sentences = re.split(r"(?<=[.!?])\s+", text)
    sentences = [s[0].lower() + s[1:] if s and rng.random() < 0.4 else s for s in sentences]
    return " ".join(sentences)


def sentence_restructure(text: str, rng: random.Random) -> str:
    sentences = [s for s in re.split(r"(?<=[.!?])\s+", text.strip()) if s]
    rebuilt = []
    for s in sentences:
        parts = re.split(r",\s+(?:and|but|because|so)\s+", s, maxsplit=1)
        if len(parts) == 2 and rng.random() < 0.7:
            second = parts[1].rstrip(".!?")
            rebuilt.append(second[0].upper() + second[1:] + ". " + parts[0].rstrip(".!?") + ".")
        else:
            rebuilt.append(s)
    if len(rebuilt) > 1 and rng.random() < 0.5:
        rebuilt = rebuilt[::-1]
    return " ".join(rebuilt)


DETERMINISTIC_TRANSFORMS = {"noise_insertion": noise_insertion, "sentence_restructure": sentence_restructure}

