def build_account_plan(config: dict) -> list[dict]:
    design = config["design"]
    topics = config["topics"]
    families = [f["id"] for f in config["prompt_families"]]
    temps = design["temperatures"]
    transforms = design["transformations_cycle"]
    repertoires = config["campaign_repertoires"]
    plan = []
    for gen in config["generators"]:
        gid = gen["generator_id"]
        for cls, n in (("coordinated", design["accounts_per_generator"]["coordinated"]),
                       ("organic_control", design["accounts_per_generator"]["organic_control"]),
                       ("professional_control", design["accounts_per_generator"]["professional_control"])):
            for k in range(n):
                repertoire = repertoires[k % len(repertoires)] if cls == "coordinated" else None
                if cls == "coordinated":
                    campaign_id = f"{gid}_camp{k % len(repertoires)}"
                    class_label, subtype = "coordinated_synthetic", "coordinated"
                elif cls == "organic_control":
                    campaign_id = f"{gid}_control_organic"
                    class_label, subtype = "simulated_human_control", "organic"
                else:
                    campaign_id = f"{gid}_control_professional"
                    class_label, subtype = "simulated_human_control", "professional"
                transformation = (transforms[k % len(transforms)]
                                  if cls in design["transformations_apply_to"]
                                  else design["professional_control_transformation"])
                plan.append({
                    "account_id": f"{gid}_{subtype}_{k:02d}",
                    "generator_id": gid,
                    "generator_family": gen["generator_family"],
                    "generator_model": gen["model"],
                    "generator_revision": gen["revision"],
                    "class_label": class_label,
                    "control_subtype": subtype,
                    "campaign_id": campaign_id,
                    "topic": topics[k % len(topics)],
                    "prompt_family": families[k % len(families)],
                    "prompt_revision": next(f["revision"] for f in config["prompt_families"]
                                            if f["id"] == families[k % len(families)]),
                    "temperature": temps[k % len(temps)],
                    "transformation_id": transformation,
                    "talking_point_repertoire": repertoire["name"] if repertoire else f"none_{subtype}_voice",
                    "style_instruction": (f"campaign_voice" if cls == "coordinated" else
                                          f"organic_persona_{k % len(ORGANIC_PERSONAS)}" if cls == "organic_control"
                                          else f"professional_org_{k % len(PROFESSIONAL_ORGS)}"),
                    "persona_index": k,
                })
    return plan

