"""Stage 2: independent multi-generator CCSF validation via OpenRouter.

Design (see stage2_config.json):
  * 3 independent generator families (Google Gemini, Alibaba Qwen, ByteDance Seed),
    verified against the live OpenRouter catalogue; a 4th family (Tencent hy3) is used
    ONLY for LLM paraphrase transforms so paraphrase never leaks a generator family
    across leave-one-family-out folds.
  * Per generator: 20 coordinated-campaign accounts, 20 uncoordinated organic-style
    control accounts, 12 vocabulary-matched professional control accounts; 8 posts per
    account, generated in a single request per account.
  * Controls are generated by the SAME model as the coordinated condition, so the
    evaluation measures coordination signal, not generator identity.
  * The professional control deliberately uses commercial-policy framing vocabulary,
    replacing the Stage 1 zero-match control.
  * Automated transformations (LLM paraphrase, seeded noise insertion, seeded sentence
    restructuring) are applied at account level to both coordinated and organic
    accounts so every transformation value contains both classes. No human editing.
  * Full provenance per response: model ID, provider, OpenRouter generation ID,
    timestamps, token usage, raw response, sampling parameters.

Phases:
  python stage2_run.py selftest                      # offline invariant checks
  python stage2_run.py build     --config stage2_config.json
  python stage2_run.py snapshot  --config stage2_config.json   # archive model catalogue
  python stage2_run.py generate  --config stage2_config.json   # calls OpenRouter (costs money)
  python stage2_run.py transform --config stage2_config.json   # paraphrase calls + seeded local transforms
  python stage2_run.py ingest    --config stage2_config.json   # corpus CSV + manifest + holdout splits
  python stage2_run.py features  --config stage2_config.json   # GPT-2 perplexity + MiniLM features
  python stage2_run.py evaluate  --config stage2_config.json   # leave-one-condition-out results
  python stage2_run.py all       --config stage2_config.json

This script never fabricates model text: `generate` writes only actual API responses,
`ingest` refuses accounts without a stored raw response, and `selftest` touches no
corpus files.
"""
