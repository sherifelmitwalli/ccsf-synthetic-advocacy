# --------------------------------------------------------------------------- utilities

def load_config(path: str | Path) -> dict:
    return json.loads(Path(path).read_text(encoding="utf-8"))


def load_env(path: Path | None = None) -> dict:
    """Minimal .env parser (KEY=VALUE, ignores comments/blank lines)."""
    env = {}
    path = path or HERE / ".env"
    if path.exists():
        for line in path.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue
            key, _, value = line.partition("=")
            env[key.strip()] = value.strip().strip('"').strip("'")
    return env


def out_dir(config: dict) -> Path:
    d = HERE / config.get("outputs_dir", "stage2_outputs")
    d.mkdir(exist_ok=True)
    return d


def write_jsonl(path: Path, rows: list[dict]) -> None:
    path.write_text("".join(json.dumps(r, ensure_ascii=False) + "\n" for r in rows), encoding="utf-8")


def read_jsonl(path: Path) -> list[dict]:
    if not path.exists():
        return []
    return [json.loads(l) for l in path.read_text(encoding="utf-8").splitlines() if l.strip()]


def utc_now() -> str:
    return _dt.datetime.now(_dt.timezone.utc).isoformat()

# --------------------------------------------------------------------------- OpenRouter client

class OpenRouterError(RuntimeError):
    pass


def _api_key(config: dict) -> str:
    import os
    env_name = config["openrouter"].get("api_key_env", "OPENROUTER_API_KEY")
    key = os.environ.get(env_name) or load_env().get(env_name)
    if not key:
        raise OpenRouterError(
            f"{env_name} not found in environment or .env. Add it before running generate/transform.")
    return key


def _http_json(url: str, payload: dict | None, headers: dict, timeout: int) -> dict:
    data = json.dumps(payload).encode("utf-8") if payload is not None else None
    req = urllib.request.Request(url, data=data, headers=headers,
                                 method="POST" if payload is not None else "GET")
    with urllib.request.urlopen(req, timeout=timeout) as resp:
        return json.loads(resp.read().decode("utf-8"))


def chat_completion(config: dict, model: str, messages: list[dict], temperature: float,
                    provider_preferences: dict | None) -> dict:
    """Call OpenRouter chat completions with retries. Returns the raw response dict."""
    oc = config["openrouter"]
    url = oc["base_url"].rstrip("/") + "/chat/completions"
    headers = {
        "Authorization": f"Bearer {_api_key(config)}",
        "Content-Type": "application/json",
        "X-Title": oc.get("app_title", "CCSF-Stage2"),
    }
    payload: dict = {
        "model": model,
        "messages": messages,
        "temperature": temperature,
        "top_p": oc.get("top_p", 0.95),
        "max_tokens": oc.get("max_tokens", 1600),
        "response_format": {"type": "json_object"},
    }
    if oc.get("disable_reasoning", True):
        payload["reasoning"] = {"enabled": False}
    if provider_preferences:
        payload["provider"] = provider_preferences
    last_error: Exception | None = None
    for attempt in range(oc.get("max_retries", 3) + 1):
        try:
            response = _http_json(url, payload, headers, oc.get("timeout_seconds", 180))
            if "error" in response:
                raise OpenRouterError(str(response["error"]))
            return response
        except urllib.error.HTTPError as exc:
            body = exc.read().decode("utf-8", "replace")[:500]
            last_error = OpenRouterError(f"HTTP {exc.code}: {body}")
            # Some providers reject response_format/reasoning: strip and retry once.
            if exc.code == 400:
                payload.pop("response_format", None)
                payload.pop("reasoning", None)
            if exc.code in (401, 402, 403):
                raise last_error
        except Exception as exc:  # network hiccups, JSON errors
            last_error = exc
        time.sleep(min(2 ** attempt * 2, 30))
    raise OpenRouterError(f"Request failed after retries: {last_error}")


def extract_posts(response: dict, expected: int) -> list[str]:
    """Parse {'posts': [...]} out of a chat completion, tolerating fenced JSON."""
    content = response["choices"][0]["message"]["content"] or ""
    text = content.strip()
    fence = re.search(r"```(?:json)?\s*(.*?)```", text, re.S)
    if fence:
        text = fence.group(1).strip()
    try:
        obj = json.loads(text)
    except json.JSONDecodeError:
        start, end = text.find("{"), text.rfind("}")
        if start == -1 or end <= start:
            raise ValueError("No JSON object in model output.")
        obj = json.loads(text[start:end + 1])
    posts = obj.get("posts") if isinstance(obj, dict) else obj
    if not isinstance(posts, list):
        raise ValueError("JSON output lacks a 'posts' array.")
    posts = [str(p).strip() for p in posts if str(p).strip()]
    # Truncate if model returns extra posts; error only if insufficient
    if len(posts) > expected:
        posts = posts[:expected]
    if len(posts) < expected:
        raise ValueError(f"Expected {expected} posts, got {len(posts)}.")
    return posts

